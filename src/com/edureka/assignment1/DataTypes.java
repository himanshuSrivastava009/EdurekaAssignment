package com.edureka.assignment1;

public class DataTypes {

	public static void main(String[] args) {
		
		int x = 10;
		int y =20;
		
		System.out.println("Sum of 2 numbers is "+ (x+y));
		System.out.println("Multiplication of 2 numbers is "+ (x*y));
		System.out.println("Division of 2 numbers is "+ (x/y));
		System.out.println("Substraction of 2 numbers is "+ (x-y));
		System.out.println("preincrement of number X is "+ (++x));
		System.out.println("preincrement of number y is "+ (++y));
		System.out.println("postincrement of number X is "+ (x++ + y));
		System.out.println("postincrement of number X is "+ (x));
		System.out.print("preDecrement of number X is "+ (--x));
		
	}

}
